class loops{
    public static void main(String[] args) {
        //while loop
        int i=0;
        while(i<5){
            System.out.println(i);
            i++;
        }

        //for loop
        for(i=0;i<10;i++){
            System.out.println("Hello");
        }

        //dowhile loop
        int a=0;
        do{
            System.out.println(a);
            a++;
        }while(a<3);
    }
}